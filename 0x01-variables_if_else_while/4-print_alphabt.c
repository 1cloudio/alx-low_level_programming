#include <stdio.h>
/**
* main - print if number is positive, negative or zero
* Description: using the main function
* this program prints programming is positive, negative or zero
* Return: 0
*/
int main(void)
{
char ch;
for (ch = 'a' ; ch <= 'z' ; ch++)
{
	putchar(ch);
}
if (ch != 'e' || ch != 'q')
{
	putchar(ch);
}
putchar('\n');
return (0);
}
